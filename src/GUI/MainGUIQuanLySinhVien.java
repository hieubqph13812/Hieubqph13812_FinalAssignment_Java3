    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        int getIndex = tbl_student.getSelectedRow();
        if (getIndex == -1) {
            JOptionPane.showMessageDialog(this, "Mời bạn chọn SV cần sửa");
            return;
        }
        if (checkEmpty() == true) {
            if (!_ServiceStudent.checkName(txt_hoTen.getText())) {
                JOptionPane.showMessageDialog(this, "Họ Tên không chứa kí tự số và kí tự đặc biệt");
            } else if (!_ServiceStudent.checkEmail(txt_email.getText())) {
                JOptionPane.showMessageDialog(this, "Email phải đúng định dạng");
            } else if (!_ServiceStudent.checkSdt(txt_sdt.getText())) {
                JOptionPane.showMessageDialog(this, "SĐT phải đúng theo tiêu chuẩn Việt Nam");
            } else if (!txt_msv.getText().equalsIgnoreCase(_ServiceStudent.getlstStudent().get(getIndex).getMsv())) {
                JOptionPane.showMessageDialog(this, "MSV không được sửa !");
                return;
            } else {

                int getChoice = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn Sửa ?", "Sửa", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (getChoice == JOptionPane.YES_OPTION) {
                    Student st;
//                    _ServiceStudent._ListStudents.set(getIndex, st = new Student(txt_msv.getText(), _ServiceStudent.chuanHoaDanhTuRieng(txt_hoTen.getText()), txt_email.getText(), txt_sdt.getText(), rd_nam.isSelected() ? true : false, txt_diaChi.getText(), _images == null ? "NO IMAGES" : _images));
//                    Student st = new Student();
                    st = _ServiceStudent.getlstStudent().get(getIndex);
                    st.setMsv(txt_msv.getText().toUpperCase());
                    st.setHoTen(_ServiceStudent.chuanHoaDanhTuRieng(txt_hoTen.getText()));
                    st.setEmail(txt_email.getText());
                    st.setSdt(txt_sdt.getText());
                    st.setDiaChi(txt_diaChi.getText());
                    st.setGioiTinh(rd_nam.isSelected() ? true : false);
                    st.setHinhAnh(_images == null ? "NO IMAGES" : _images);
                    _ServiceStudent.editStudentToDB(st);
                    try {
                        _ServiceStudent.getListStudentDB();
                    } catch (SQLException ex) {
                        Logger.getLogger(MainGUIQuanLySinhVien.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    LoadTable();
                    JOptionPane.showMessageDialog(this, "Bạn đã sửa thành công !");
                } else {
                    JOptionPane.showMessageDialog(this, "Sửa không thành công");
                }

            }
        }
    }//GEN-LAST:event_btn_updateActionPerformed

    private void tbl_studentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_studentMouseClicked
        int getIndex = tbl_student.getSelectedRow();
        Student st = new Student();
        st = _ServiceStudent.getlstStudent().get(getIndex);
        txt_msv.setText(st.getMsv());
        txt_hoTen.setText(st.getHoTen());
        txt_email.setText(st.getEmail());
        txt_sdt.setText(st.getSdt());
        txt_diaChi.setText(st.getDiaChi());
        if (st.getGioiTinh() == true) {
            rd_nam.setSelected(true);
        } else {
            rd_nu.setSelected(true);
        }

        if (st.getHinhAnh().equalsIgnoreCase("NO IMAGES")) {
            lbl_images.setText("NO IMAGES");
            lbl_images.setIcon(null);
        } else {
            _images = _ServiceStudent.getlstStudent().get(getIndex).getHinhAnh();
            String url = "images\\" + st.getHinhAnh();
            lbl_images.setText("");
            ImageIcon imgIcon = new ImageIcon(url);
            Image img = imgIcon.getImage();
            lbl_images.setIcon(new ImageIcon(img.getScaledInstance(lbl_images.getWidth(), lbl_images.getHeight(), 0)));
            try {
                lbl_images.setIcon((Icon) img);
            } catch (Exception e) {
            }
        }

    }//GEN-LAST:event_tbl_studentMouseClicked

