    public boolean checkEmpty() {
        if (txt_msv.getText().isEmpty() || txt_hoTen.getText().isEmpty() || txt_sdt.getText().isEmpty() || txt_email.getText().isEmpty() || txt_diaChi.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mời bạn nhập đầy đủ thông tin vào các ô");
            return false;
        }
        if (txt_msv.getText().isBlank() || txt_hoTen.getText().isBlank() || txt_sdt.getText().isBlank() || txt_email.getText().isBlank() || txt_diaChi.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Bạn không được nhập khoảng trắng vào bất kỳ ô nào \nVui lòng nhập lại");
            return false;
        }
        return true;
    }

    public void LoadTable() {
        _DefaultTableModel.setRowCount(0);
        _DefaultTableModel = (DefaultTableModel) tbl_student.getModel();
        for (Student x : _ServiceStudent.getlstStudent()) {
            _DefaultTableModel.addRow(new Object[]{x.getMsv(), x.getHoTen(), x.getEmail(), x.getSdt(), x.getGioiTinh() == true ? "Nam" : "Nữ", x.getDiaChi(), x.getHinhAnh()});
        }
    }

    public void clearForm() {
        txt_msv.setText("");
        txt_hoTen.setText("");
        txt_email.setText("");
        txt_sdt.setText("");
        txt_diaChi.setText("");
        rd_nam.setSelected(true);
        lbl_images.setIcon(null);
        lbl_images.setText("IMG (click vào đây)");
    }

