    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed

        if (checkEmpty() == true) {
            if (!_ServiceStudent.checkName(txt_hoTen.getText())) {
                JOptionPane.showMessageDialog(this, "Họ Tên không chứa kí tự số và kí tự đặc biệt");
            } else if (!_ServiceStudent.checkEmail(txt_email.getText())) {
                JOptionPane.showMessageDialog(this, "Email phải đúng định dạng");
            } else if (!_ServiceStudent.checkSdt(txt_sdt.getText())) {
                JOptionPane.showMessageDialog(this, "SĐT phải đúng theo tiêu chuẩn Việt Nam");
            } else {
                _Student = new Student(txt_msv.getText().toUpperCase(), _ServiceStudent.chuanHoaDanhTuRieng(txt_hoTen.getText()), txt_email.getText(), txt_sdt.getText(), rd_nam.isSelected() ? true : false,
                        txt_diaChi.getText(), lbl_images.getText());
                _Student.setHinhAnh(_images == null ? "NO IMAGES" : _images);
                if (_ServiceStudent.checkMsv(_Student)) {
                    JOptionPane.showMessageDialog(this, "MSV tồn tại !");
                    txt_msv.setText("");
                } else
                    
                    try {
                    int getChoice = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn Thêm không ?", "Thêm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                    if (getChoice == JOptionPane.YES_OPTION) {
                        if (_ServiceStudent.addStudent(_Student)) {
                            JOptionPane.showMessageDialog(this, "Thêm thành công");
                            _ServiceStudent.getListStudentDB();
                            LoadTable();
                        } else {
                            JOptionPane.showMessageDialog(this, "Thất bại");
                        }
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(MainGUIQuanLySinhVien.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btn_saveActionPerformed

